DATAS SEGMENT
    STRING DB 'abcdefg$'
    TIP DB 'NOT FOUND$'
DATAS ENDS

CODES SEGMENT
   ASSUME CS:CODES,DS:DATAS
START:
MOV AX,DATAS
MOV ES,AX
MOV DS,AX
MOV AH,01H
INT 21H 
;键盘输入一个字符并回显
LEA DI,STRING
MOV CX,8
CLD
MOV BL,'0'
FIND: INC BL
SCASB
LOOPNZ FIND
JZ OUTPUT
NOFIND:
MOV DL,0AH
MOV AH,02H
INT 21H
;显示字符
LEA DX,TIP
MOV AH,09H
INT 21H
;显示字符串
JMP DDL
OUTPUT: 
MOV DL,0AH
MOV AH,2H
INT 21H
;显示输出一个字符
MOV DL,BL
MOV AH,2H
INT 21H
;显示输出一个字符
JMP DDL
DDL:    
MOV AH,4CH
INT 21H
;程序结束
CODES ENDS
END START